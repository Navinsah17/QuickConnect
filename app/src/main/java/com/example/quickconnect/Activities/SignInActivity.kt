class SignInActivity : AppCompatActivity() {
    lateinit var bindingsignin : ActivitySignInBinding
    private val RC_SIGN_IN: Int = 123
    private val TAG = "SignInActivity Tag"
    private lateinit var googleSignInClient: GoogleSignInClient
    private lateinit var auth: FirebaseAuth
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        bindingsignin = ActivitySignInBinding.inflate(layoutInflater)
        window.requestFeature(Window.FEATURE_NO_TITLE)
        window.setFlags(
            WindowManager.LayoutParams.FLAG_FULLSCREEN,
            WindowManager.LayoutParams.FLAG_FULLSCREEN
        )
        setContentView(bindingsignin.root)
        //auth = Firebase.auth

        val gso = GoogleSignInOptions.Builder(GoogleSignInOptions.DEFAULT_SIGN_IN)
            .requestIdToken(getString(R.string.your_web_client_id))
            .requestEmail()
            .build()
        googleSignInClient = GoogleSignIn.getClient(this, gso)
        auth = Firebase.auth

        bindingsignin.signInButton.setOnClickListener {
            signIn()
        }
        bindingsignin.signupmobile.setOnClickListener {
            val intent = Intent(this, MainActivity::class.java)
            startActivity(intent)

        }




    }
    override fun onStart() {
        super.onStart()
        // Check if user is signed in (non-null) and update UI accordingly.
        val currentUser = auth.currentUser
        updateUI(currentUser)
    }

    private fun signIn(){
        val signInIntent = googleSignInClient.signInIntent
        startActivityForResult(signInIntent, RC_SIGN_IN)
    }

    override fun onActivityResult(requestCode: Int, resultCode: Int, data: Intent?) {
        super.onActivityResult(requestCode, resultCode, data)

        if (requestCode == RC_SIGN_IN) {
            val task = GoogleSignIn.getSignedInAccountFromIntent(data)
            handleSignInResult(task)
        }
    }
    private fun handleSignInResult(completedTask: Task<GoogleSignInAccount>) {
        try {
            val account =
                completedTask.getResult(ApiException::class.java)!!
            Log.d(TAG, "firebaseAuthWithGoogle:" + account.id)
            firebaseAuthWithGoogle(account.idToken!!)
        } catch (e: ApiException) {
            Log.w(TAG, "signInResult:failed code=" + e.statusCode)

        }
    }

    private fun firebaseAuthWithGoogle(idToken: String) {
        val credential = GoogleAuthProvider.getCredential(idToken, null)
        bindingsignin.signInButton.visibility = View.GONE
        bindingsignin.progressBar.visibility = View.VISIBLE
        GlobalScope.launch(Dispatchers.IO) {
            val auth = auth.signInWithCredential(credential).await()
            val firebaseUser = auth.user
            withContext(Dispatchers.Main) {
                updateUI(firebaseUser)
            }
        }

    }

    @SuppressLint("RestrictedApi")
    private fun updateUI(firebaseUser: FirebaseUser?) {

        if(firebaseUser != null) {

            val user = com.example.quickconnect.models.User(
                firebaseUser.uid,
                firebaseUser.displayName,
                firebaseUser.photoUrl.toString(),
                firebaseUser.email.toString(),
                firebaseUser.phoneNumber.toString()
            )
            val usersDao = UserDao()
            usersDao.addUser(user)

            val dashboardActivityIntent = Intent(this, DashboardActivity::class.java)
            startActivity(dashboardActivityIntent)
            finish()

        } else {
            bindingsignin.signInButton.visibility = View.VISIBLE
            bindingsignin.progressBar.visibility = View.GONE
        }
    }


    }


